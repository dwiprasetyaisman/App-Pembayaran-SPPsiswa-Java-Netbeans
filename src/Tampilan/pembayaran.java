    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");     
        String fd = sdf.format(jtgl.getValue());        
        String sql = "insert into pembayaran values (?,?,?)";  
        String zsql = "insert into isi values (?,?,?,?,?)";    
        try{   
            PreparedStatement stat = conn.prepareStatement(sql);       
            stat.setString(1, txtid.getText());            
            stat.setString(2, fd);            
            stat.setString(3, txtnis.getText()); 
            
             stat.executeUpdate();    
             
             int t = tblspp.getRowCount();   
             for(int i=0; i < t ; i++)             
             {   
                
                String xkd = tblspp.getValueAt(i, 0).toString(); 
                String xbln = tblspp.getValueAt(i, 4).toString(); 
                String xjml = tblspp.getValueAt(i, 5).toString();  
                
                
                PreparedStatement stat2 = conn.prepareStatement(zsql); 
                stat2.setString(1, txtid.getText());
                stat2.setString(2, xkd);
                stat2.setString(3, cbkelas.getSelectedItem().toString());
                stat2.setString(4, xbln);
                stat2.setString(5, xjml);
                 
                
                stat2.executeUpdate();          
             }             
             JOptionPane.showMessageDialog(null, "data berhasil disimpan");        
        }         catch (SQLException e){         
             JOptionPane.showMessageDialog(null, "data gagal disimpan"+e);         
        }   
        //kosong();             
        aktif();             
        autonumber(); 
        
    }//GEN-LAST:event_beditActionPerformed

