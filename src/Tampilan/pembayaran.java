    protected void autonumber(){   
        String idbyr="";        
        try{                
            String sql = "SELECT idbyr FROM pembayaran order by idbyr asc";               
            PreparedStatement stat = conn.prepareStatement(sql);             
            ResultSet rs=stat.executeQuery(sql);                            
            while(rs.next()){                    
                idbyr=rs.getString("idbyr");       
            }          
        } catch(SQLException sqle){idbyr="";}   
 
       if(idbyr.length() <1){idbyr="ID0000";}     
       String ur=idbyr.substring(2);     
       int u=Integer.parseInt(ur)+1;     
       System.out.println(ur+"=="+u);   
       if(u<10)           
       {ur="000"+u;}      
       else if(u<100)     
       {ur="00"+u;}       
       else if(u<1000)    
       {ur="0"+u;}       
       else               
       {ur=""+u;}       
       idbyr  ="ID"+ur;  
       txtid.setText(idbyr); 
    } 

